#include "3-calc.h"

/**
 * op_add - calculates the sum of given two inputs
 *
 * @a: first input
 * @b: second input
 *
 * Return: calculation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two given inputs
 *
 * @a: first input
 * @b: second input
 *
 * Return: calculation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two given inputs
 *
 * @a: first input
 * @b: second input
 *
 * Return: calculation
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two given inputs
 *
 * @a: first input
 * @b: second input
 *
 * Return: calculation
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gets the remainder two given inputs
 *
 * @a: first input
 * @b: second input
 *
 * Return: calculation
 */

int op_mod(int a, int b)
{
	return (a % b);
}
